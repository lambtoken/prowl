function love.conf(t)
    t.window.fullscreen = false
    t.window.icon = 'assets/icon.png'
    t.window.depth = 16
    t.window.title = "Prowl"
    t.window.resizable = true
    t.window.width = 1280
    t.window.height = 720
    t.window.minwidth = 640
    t.window.minheight = 480
    t.console = true
    t.window.vsync = 0
end